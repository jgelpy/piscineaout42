int		ft_is_prime(int nb)
{
	int	i;

	if (nb < 2 || nb > 2147483647)
		return (0);
	else if (nb == 2 || nb == 3)
		return (1);
	else if (nb % 2 == 0)
		return (0);
	i = 3;
	while (i * i <= nb && nb % i != 0 && i < 46341)
		i += 2;
	if (nb % i == 0)
		return (0);
	else
		return (1);
}
