int		ft_sqrt(int nb)
{
	int i;

	if (nb < 0 || nb > 2147483647)
		return (0);
	i = nb % 2;
	while (i * i < nb && i < 46340)
		i += 2;
	if (i * i == nb)
		return (i);
	else
		return (0);
}
